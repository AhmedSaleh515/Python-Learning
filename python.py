first_number = int (print("enter the first number: "))
operator = print("enter an operator")
second_number = int(print("enter the second number")) 

if operator == '+':
    print((first_number + second_number))
elif operator == '-':
    print((first_number - second_number))
elif operator == '*':
    print((first_number * second_number))
elif operator == '/':
    print((first_number / second_number))        
elif operator == '%':
    print((first_number % second_number))
